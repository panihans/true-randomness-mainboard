typedef struct Command {
	int32_t motor1;
	int32_t motor2;
	int32_t motor3;
	int32_t thrower;
	int32_t servo;
	int32_t ir;
	int32_t delimiter;
} Command;

Command command = {.motor1 = 0, .motor2 = 0, .motor3 = 0, .thrower = 0, .servo = 0, .ir = 0, .delimiter = 0};
Command feedback = {.motor1 = 0, .motor2 = 0, .motor3 = 0, .thrower = 0, .servo = 0, .ir = 0,  	 .delimiter = 0};

